                    <h5>GENERAL OBJECTIVES OF B.Ed. COURSE :-</h5>
                    <p>Enable the student teacher</p>
                    <ul>
                        <li>- Pupil teachers will be able to understand the nature , purpose and philosophy of secondary education.</li>
                        <li>- Prospective teachers will be able to understand the psychology of their pupils and the process of socialization.</li>
                        <li>- They will be able to acquire competencies relevant to stage specific pedagogy, curriculum development, its transaction, evaluation and skills for guidance and counseling.</li>
                        <li>- They will be able to foster creative thinking among pupils for reconstruction of knowledge.</li>
                        <li>- They will be acquainted with factors and forces affecting educational system and needs of special group of pupils.</li>
                        <li>- They will be able to utilize information system and community resources as educational inputs.</li>
                        <li>- They will be able to develop awareness about role of education in building up a democratic , secular and socialistic pattern of society.</li>
                        <li>- They will be able to develop aesthetic sensibilities and research aptitude.</li>
